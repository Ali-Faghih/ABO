export const ALL_CITIES = IRAN_PROVINCES.flatMap((p) => p.cities);

export function getCitiesByProvince(province: string): string[] {
  const found = IRAN_PROVINCES.find((p) => p.name === province);
  return found ? found.cities : [];
}

export function getProvinceByCity(city: string): string | undefined {
  return IRAN_PROVINCES.find((p) => p.cities.includes(city))?.name;
}

